  //Returns address of ASCII representation for regular ASCII chars. If it's an extended char, returns a blank character.
  unsigned char * charBitmap(int c)
  {
		if(c >= ' ' && c <= '~')
    	return font_bitmap + CHAR_HEIGHT * (c -' ' +1);
		return font_bitmap;
  }

